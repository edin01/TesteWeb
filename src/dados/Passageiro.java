package dados;

public class Passageiro {
	
	String nmPass;
	String nascPass;
	long cpfPass;
	String sxPass;
	
	public String getNmPass() {
		return nmPass;
	}
	public void setNmPass(String nmPass) {
		this.nmPass = nmPass;
	}
	public String getNascPass() {
		return nascPass;
	}
	public void setNascPass(String nascPass) {
		this.nascPass = nascPass;
	}
	public long getCpfPass() {
		return cpfPass;
	}
	public void setCpfPass(long cpfPass) {
		this.cpfPass = cpfPass;
	}
	public String getSxPass() {
		return sxPass;
	}
	public void setSxPass(String sxPass) {
		this.sxPass = sxPass;
	}

}
