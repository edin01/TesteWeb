package dados;

public class Motorista {
	
	int idMot;
	String nmMot;
	String nascMot;
	long cpfMot;
	String modelo;
	String status;
	String sexo;
	
	public int getIdMot() {
		return idMot;
	}
	public void setIdMot(int idMot) {
		this.idMot = idMot;
	}
	public String getNmMot() {
		return nmMot;
	}
	public void setNmMot(String nmMot) {
		this.nmMot = nmMot;
	}
	public String getNascMot() {
		return nascMot;
	}
	public void setNascMot(String nascMot) {
		this.nascMot = nascMot;
	}
	public long getCpfMot() {
		return cpfMot;
	}
	public void setCpfMot(long cpfMot) {
		this.cpfMot = cpfMot;
	}
	public String getModelo() {
		return modelo;
	}
	public void setModelo(String modelo) {
		this.modelo = modelo;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getSexo() {
		return sexo;
	}
	public void setSexo(String sexo) {
		this.sexo = sexo;
	}
	
}
