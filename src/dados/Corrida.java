package dados;

public class Corrida {
	
	double valor;
	int idPassageiro;
	int idMotorista;
	String nmMotorista;
	String nmPassageiro;
	public String status;
	
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public double getValor() {
		return valor;
	}
	public void setValor(double valor) {
		this.valor = valor;
	}
	public int getIdPassageiro() {
		return idPassageiro;
	}
	public void setIdPassageiro(int idPassageiro) {
		this.idPassageiro = idPassageiro;
	}
	public int getIdMotorista() {
		return idMotorista;
	}
	public void setIdMotorista(int idMotorista) {
		this.idMotorista = idMotorista;
	}
	public String getNmMotorista() {
		return nmMotorista;
	}
	public void setNmMotorista(String nmMotorista) {
		this.nmMotorista = nmMotorista;
	}
	public String getNmPassageiro() {
		return nmPassageiro;
	}
	public void setNmPassageiro(String nmPassageiro) {
		this.nmPassageiro = nmPassageiro;
	}
	
	

}
